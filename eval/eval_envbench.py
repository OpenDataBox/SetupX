"""
Re-adjudicate envBench baseline results with our Prosecutor + Judge criteria.

Usage:
    .venv/bin/python scripts/eval_envbench.py [--repos repo1,repo2,...] [--workers N]

Workflow:
    1. Read bootstrap scripts from envBench/output/scripts.jsonl
    2. Start the envbench-python container
    3. Clone the repository inside the container and run the bootstrap script
    4. Investigate the environment with ProsecutorAgent
    5. Adjudicate with JudgeAgent (if prosecuted)
    6. Aggregate the report
"""

import argparse
import json
import os
import subprocess
import sys
import tempfile
import threading
import time
from concurrent.futures import ThreadPoolExecutor, as_completed
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from src.environment_manager import EnvironmentManager
from src.prosecutor_agent import ProsecutorAgent
from src.judge_agent import JudgeAgent
from src.logger import get_logger

logger = get_logger("eval_envbench")

ENVBENCH_IMAGE = "envbench-python:local"
ENVBENCH_DOCKERFILE = Path(__file__).resolve().parent.parent / "envBench" / "dockerfiles" / "python.Dockerfile"
SCRIPTS_JSONL = Path(__file__).resolve().parent.parent / "envBench" / "output" / "scripts.jsonl"
ENVBENCH_EVAL_RESULTS = Path(__file__).resolve().parent.parent / "envBench" / "output" / "100xpu_eval" / "results.jsonl"
OUTPUT_DIR = Path(__file__).resolve().parent.parent / "eval" / "results"

# Thread-safe lock for writing results
_results_lock = threading.Lock()


def ensure_base_image() -> None:
    """Ensure the envbench-python base image exists; build it if missing."""
    result = subprocess.run(
        ["docker", "images", "-q", ENVBENCH_IMAGE],
        capture_output=True, text=True,
    )
    if result.stdout.strip():
        logger.info(f"Base image already exists: {ENVBENCH_IMAGE}")
        return

    logger.info(f"Building the envbench-python base image (first run, takes about 10 minutes)...")
    build_ctx = ENVBENCH_DOCKERFILE.parent
    result = subprocess.run(
        ["docker", "build", "-t", ENVBENCH_IMAGE, "-f", str(ENVBENCH_DOCKERFILE), str(build_ctx)],
        capture_output=True, text=True, timeout=3600,
    )
    if result.returncode != 0:
        logger.error(f"Image build failed:\n{result.stderr[-1000:]}")
        sys.exit(1)
    logger.info("Base image build complete")


def load_scripts() -> dict[str, dict]:
    """Load scripts.jsonl, returning {repository: {repository, revision, script}}."""
    data = {}
    with open(SCRIPTS_JSONL, encoding="utf-8") as f:
        for line in f:
            if not line.strip():
                continue
            rec = json.loads(line)
            data[rec["repository"]] = rec
    return data


def load_envbench_eval() -> dict[str, dict]:
    """Load the original envBench evaluation results for comparison."""
    data = {}
    with open(ENVBENCH_EVAL_RESULTS, encoding="utf-8") as f:
        for line in f:
            if not line.strip():
                continue
            rec = json.loads(line)
            data[rec["repo_name"]] = rec
    return data


def start_container(image: str) -> str | None:
    """Start a container and return its container_id."""
    result = subprocess.run(
        ["docker", "run", "-d", image, "sleep", "infinity"],
        capture_output=True, text=True,
    )
    if result.returncode != 0:
        logger.warning(f"Failed to start container: {result.stderr}")
        return None
    cid = result.stdout.strip()
    logger.info(f"Container started: {cid[:12]}")
    return cid


def stop_container(container_id: str) -> None:
    """Stop and remove the container."""
    subprocess.run(["docker", "rm", "-f", container_id], capture_output=True)
    logger.info(f"Container destroyed: {container_id[:12]}")


def exec_in_container(container_id: str, cmd: str, timeout: int = 600) -> tuple[int, str]:
    """Run a command inside the container and return (exit_code, output)."""
    result = subprocess.run(
        ["docker", "exec", container_id, "bash", "-c", cmd],
        capture_output=True, text=True, timeout=timeout,
    )
    output = result.stdout + result.stderr
    return result.returncode, output


def build_setup_history_from_script(repo: str, script: str) -> list[dict]:
    """Convert the bootstrap script into a pseudo setup_history so the Prosecutor knows what was installed."""
    return [{
        "step": 0,
        "action": {
            "action_type": "CONTEXT",
            "thought": "The following is the bootstrap script generated by the envBench Agent, showing the environment setup process.",
            "content": {},
        },
        "result": {
            "exit_code": 0,
            "stdout": f"=== envBench Bootstrap Script for {repo} ===\n{script}",
            "stderr": "",
        },
    }]


def build_verify_messages(repo: str, envbench_result: dict | None) -> list[dict]:
    """Build verify_messages to give the Prosecutor background context."""
    msgs = [
        {
            "role": "user",
            "content": (
                "[IMPORTANT] This is an environment configured by the envBench Agent. "
                "envBench's success criterion is pyright reportMissingImports being 0 (no missing imports under static analysis); "
                "it does not require tests to actually pass.\n\n"
                "Please investigate using our criteria: are the core dependencies importable? Can the tests actually run?"
            ),
        },
    ]

    if envbench_result:
        eb_exit = envbench_result.get("exit_code", "?")
        eb_issues = envbench_result.get("issues_count", "?")
        msgs.append({
            "role": "user",
            "content": f"envBench original evaluation: exit_code={eb_exit}, reportMissingImports={eb_issues}",
        })

    return msgs


def _inject_bootstrap_env(container_id: str, env: EnvironmentManager) -> None:
    """Restore key variables from the environment snapshot dumped after bootstrap.

    The bootstrap script runs via `source`, so its conda activate / pyenv global / export PATH
    effects only live in that shell. They are all lost when exec_run opens a new shell.
    Fix: at the end of bootstrap, run `env > /tmp/_bootstrap_env.txt`, then read it back and inject here.
    """
    # Key variables to inject (commonly modified by bootstrap scripts)
    IMPORTANT_VARS = {
        "PATH", "PYTHONPATH", "PYTHONHOME",
        # pyenv
        "PYENV_VERSION", "PYENV_ROOT", "PYENV_SHELL",
        # conda
        "CONDA_DEFAULT_ENV", "CONDA_PREFIX", "CONDA_EXE",
        "CONDA_PYTHON_EXE", "CONDA_SHLVL",
        # virtualenv / venv
        "VIRTUAL_ENV",
        # poetry
        "POETRY_HOME", "POETRY_VIRTUALENVS_IN_PROJECT",
        # other
        "LD_LIBRARY_PATH", "PKG_CONFIG_PATH", "CMAKE_PREFIX_PATH",
    }

    try:
        exit_code, output = exec_in_container(
            container_id, "cat /tmp/_bootstrap_env.txt", timeout=10,
        )
        if exit_code != 0:
            logger.warning(f"Failed to read bootstrap environment variables: {output[:200]}")
            return
    except Exception as e:
        logger.warning(f"Error reading bootstrap environment variables: {e}")
        return

    injected = 0
    for line in output.splitlines():
        # env output format: KEY=VALUE (VALUE may contain =)
        if "=" not in line:
            continue
        key, _, value = line.partition("=")
        key = key.strip()
        if key in IMPORTANT_VARS and value.strip():
            env.set_env(key, value)
            injected += 1
            logger.debug(f"Injected environment variable: {key}={value[:80]}")

    logger.info(f"Injected {injected} variables from the bootstrap environment snapshot")


def evaluate_repo(
    repo: str,
    revision: str,
    script: str,
    envbench_result: dict | None,
) -> dict:
    """Evaluate a single repository."""
    result = {
        "repo": repo,
        "revision": revision,
        "container_ok": False,
        "script_ok": False,
        "prosecute": None,
        "charges": [],
        "verdict": None,
        "reason": "",
        "envbench_exit_code": envbench_result.get("exit_code") if envbench_result else None,
        "envbench_issues": envbench_result.get("issues_count") if envbench_result else None,
    }

    # 1. Start the container
    container_id = start_container(ENVBENCH_IMAGE)
    if not container_id:
        result["reason"] = "Failed to start container"
        return result
    result["container_ok"] = True

    try:
        # 2. Clone the repository inside the container
        # envBench in-container path: /data/project/{org__repo@sha}/
        safe_name = repo.replace("/", "__")
        repo_dir_name = f"{safe_name}@{revision}"
        container_repo_path = f"/data/project/{repo_dir_name}"

        logger.info(f"[{repo}] cloning repository to {container_repo_path}")
        clone_cmd = (
            f"mkdir -p {container_repo_path} && "
            f"git clone --depth 1 https://github.com/{repo}.git {container_repo_path} && "
            f"cd {container_repo_path} && git fetch --depth 1 origin {revision} && git checkout {revision}"
        )
        exit_code, output = exec_in_container(container_id, clone_cmd, timeout=300)
        if exit_code != 0:
            # Some shas may not be reachable with depth 1; try a full clone
            logger.info(f"[{repo}] shallow clone failed, trying a full clone")
            clone_cmd2 = (
                f"rm -rf {container_repo_path} && "
                f"git clone https://github.com/{repo}.git {container_repo_path} && "
                f"cd {container_repo_path} && git checkout {revision}"
            )
            exit_code, output = exec_in_container(container_id, clone_cmd2, timeout=600)
            if exit_code != 0:
                result["reason"] = f"clone failed: {output[-500:]}"
                stop_container(container_id)
                return result

        # 3. Run the bootstrap script
        logger.info(f"[{repo}] running bootstrap script ({len(script)} chars)")
        # Write the script into the container, then source it
        with tempfile.NamedTemporaryFile(mode="w", suffix=".sh", delete=False) as tmp:
            tmp.write(script)
            tmp_path = tmp.name
        try:
            subprocess.run(
                ["docker", "cp", tmp_path, f"{container_id}:{container_repo_path}/bootstrap_script.sh"],
                capture_output=True, check=True,
            )
        finally:
            os.unlink(tmp_path)

        # Run the script with bash source (consistent with envBench's python_build.sh)
        # set +e prevents a single command failure from exiting the whole script
        # After running, dump environment variables to a file for the prosecutor to inherit
        # trap EXIT ensures the env dump runs even if the bootstrap script uses set -e and exits early
        exec_cmd = (
            f"cd {container_repo_path} && trap 'env > /tmp/_bootstrap_env.txt' EXIT && "
            f"source bootstrap_script.sh 2>&1; echo EXIT_CODE=$?"
        )
        exit_code, output = exec_in_container(container_id, exec_cmd, timeout=600)
        # Extract the script's own exit code from the tail of the output
        script_exit = exit_code
        if "EXIT_CODE=" in output:
            try:
                script_exit = int(output.rsplit("EXIT_CODE=", 1)[1].strip().split()[0])
            except (ValueError, IndexError):
                pass
        result["script_ok"] = True
        logger.info(f"[{repo}] bootstrap script finished, exit_code={script_exit}")

        # 4. Take over the container and run Prosecutor + Judge
        env = EnvironmentManager()
        env.attach(container_id, repo_dir=container_repo_path)

        # Restore key variables from the post-bootstrap environment snapshot
        # so the prosecutor's exec_run inherits conda activate / pyenv global / export PATH effects
        _inject_bootstrap_env(container_id, env)

        setup_history = build_setup_history_from_script(repo, script)
        verify_messages = build_verify_messages(repo, envbench_result)

        # 5. Prosecutor investigation
        logger.info(f"[{repo}] prosecutor starting investigation")
        prosecutor = ProsecutorAgent(env, setup_history, verify_messages)
        prosecution = prosecutor.investigate()
        result["prosecute"] = prosecution.prosecute
        result["charges"] = prosecution.charges
        logger.info(f"[{repo}] prosecutor conclusion: prosecute={prosecution.prosecute}, charges={len(prosecution.charges)}")

        if not prosecution.prosecute:
            result["verdict"] = "not_guilty"
            result["reason"] = "Prosecutor did not prosecute"
        else:
            # 6. Judge adjudication
            logger.info(f"[{repo}] judge starting adjudication")
            judgment = JudgeAgent(
                setup_history,
                verify_messages,
                prosecution,
                env=env,
            ).rule()
            result["verdict"] = judgment["verdict"]
            result["reason"] = judgment["reasoning"]
            logger.info(f"[{repo}] judge verdict: {judgment['verdict']}")

    except Exception as e:
        logger.warning(f"[{repo}] evaluation error: {e}")
        result["reason"] = f"evaluation error: {e}"
    finally:
        stop_container(container_id)

    return result


def _save_results(results: list[dict], output_path: Path) -> None:
    """Write the results file in a thread-safe way."""
    with _results_lock:
        with open(output_path, "w", encoding="utf-8") as f:
            json.dump(results, f, ensure_ascii=False, indent=2)


def _run_one(
    repo: str,
    scripts: dict[str, dict],
    envbench_eval: dict[str, dict],
    results: list[dict],
    output_path: Path,
    total: int,
) -> dict:
    """Evaluation entry point for a single repository (called by the thread pool)."""
    rec = scripts[repo]
    eb_result = envbench_eval.get(repo)

    logger.info(f"Evaluating: {repo}")
    try:
        result = evaluate_repo(repo, rec["revision"], rec["script"], eb_result)
    except Exception as e:
        logger.error(f"[{repo}] uncaught exception: {e}")
        result = {
            "repo": repo, "revision": rec["revision"],
            "container_ok": False, "script_ok": False,
            "prosecute": None, "charges": [], "verdict": None,
            "reason": f"uncaught exception: {e}",
        }

    with _results_lock:
        results.append(result)
        count = len(results)

    _save_results(results, output_path)

    status = ("✅ not_guilty" if result.get("verdict") == "not_guilty" else
              "❌ guilty" if result.get("verdict") == "guilty" else
              "⚠️ " + result.get("reason", "?"))
    eb_info = f" (envBench: exit={result.get('envbench_exit_code')}, issues={result.get('envbench_issues')})"
    print(f"  [{count}/{total}] {repo}: {status}{eb_info}")
    return result


def main():
    parser = argparse.ArgumentParser(description="Re-adjudicate envBench baseline results")
    parser.add_argument("--repos", type=str, default=None,
                        help="comma-separated repository names (org/repo); default is all")
    parser.add_argument("--workers", type=int, default=1,
                        help="number of parallel workers (default 1; no more than 3 recommended on Mac)")
    parser.add_argument("--output", type=str, default=None,
                        help="output file path; default is results/envbench_100_eval.json")
    args = parser.parse_args()

    # Ensure the base image exists
    ensure_base_image()

    # Load data
    scripts = load_scripts()
    envbench_eval = load_envbench_eval()
    logger.info(f"Loaded bootstrap scripts for {len(scripts)} repositories")

    # Determine the list to evaluate
    if args.repos:
        repos = [r.strip() for r in args.repos.split(",")]
    else:
        repos = sorted(scripts.keys())

    # Output path
    output_path = Path(args.output) if args.output else OUTPUT_DIR / "envbench_100_eval.json"
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # Load existing results (resume support)
    existing_results = {}
    if output_path.exists():
        try:
            with open(output_path, "r", encoding="utf-8") as f:
                for r in json.load(f):
                    existing_results[r["repo"]] = r
            logger.info(f"Loaded {len(existing_results)} historical results")
        except Exception as e:
            logger.warning(f"Failed to load historical results: {e}")

    results = list(existing_results.values())

    # Filter out the repositories still pending
    pending = [r for r in repos if r in scripts and r not in existing_results]
    skipped = [r for r in repos if r not in scripts]
    for r in skipped:
        logger.warning(f"[{r}] no bootstrap script found, skipping")

    logger.info(f"{len(pending)} repositories pending ({len(existing_results)} historical results already present), workers={args.workers}")

    if args.workers <= 1:
        # Serial
        for repo in pending:
            _run_one(repo, scripts, envbench_eval, results, output_path, len(repos))
    else:
        # Parallel
        with ThreadPoolExecutor(max_workers=args.workers) as executor:
            futures = {
                executor.submit(_run_one, repo, scripts, envbench_eval, results, output_path, len(repos)): repo
                for repo in pending
            }
            for future in as_completed(futures):
                repo = futures[future]
                try:
                    future.result()
                except Exception as e:
                    logger.error(f"[{repo}] worker error: {e}")

    # Final write
    _save_results(results, output_path)

    # Print summary
    print(f"\n{'='*60}")
    print("Evaluation summary")
    print(f"{'='*60}")
    total = len(results)
    container_ok = sum(1 for r in results if r.get("container_ok"))
    script_ok = sum(1 for r in results if r.get("script_ok"))
    not_guilty = sum(1 for r in results if r.get("verdict") == "not_guilty")
    guilty = sum(1 for r in results if r.get("verdict") == "guilty")
    no_verdict = total - not_guilty - guilty

    print(f"Total: {total}")
    print(f"Containers started successfully: {container_ok}")
    print(f"Scripts executed successfully: {script_ok}")
    print(f"not_guilty (also passes our criteria): {not_guilty}")
    print(f"guilty (fails our criteria): {guilty}")
    print(f"No verdict: {no_verdict}")
    print(f"Results written to: {output_path}")


if __name__ == "__main__":
    main()
